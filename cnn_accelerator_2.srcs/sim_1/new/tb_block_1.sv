`timescale 1ns / 1ps

module tb_block_1;

    reg clk;
    reg rst;
    reg inp_valid;

    // =====================================================
    // IMAGE
    // =====================================================

    reg [7:0] image_mem [783:0];

    reg signed [31:0] image [783:0];

    // =====================================================
    // WEIGHTS
    // =====================================================

    reg signed [7:0] weight0_mem [24:0];
    reg signed [7:0] weight1_mem [24:0];
    reg signed [7:0] weight2_mem [24:0];

    reg signed [31:0] weight_0_0 [24:0];
    reg signed [31:0] weight_0_1 [24:0];
    reg signed [31:0] weight_0_2 [24:0];

    // =====================================================
    // BIAS
    // =====================================================

    reg signed [7:0] bias_mem [2:0];

    reg signed [31:0] bias [2:0];

    // =====================================================
    // OUTPUTS
    // =====================================================

    wire signed [31:0] rout_0 [143:0];
    wire signed [31:0] rout_1 [143:0];
    wire signed [31:0] rout_2 [143:0];

    wire valid_0;
    wire valid_1;
    wire valid_2;

    integer i;

    // =====================================================
    // DUT
    // =====================================================

    block_1 dut(
        .clk(clk),
        .rst(rst),
        .image(image),
        .bias(bias),
        .inp_valid(inp_valid),

        .weight_0_0(weight_0_0),
        .weight_0_1(weight_0_1),
        .weight_0_2(weight_0_2),

        .rout_0(rout_0),
        .valid_0(valid_0),

        .rout_1(rout_1),
        .valid_1(valid_1),

        .rout_2(rout_2),
        .valid_2(valid_2)
    );

    // =====================================================
    // CLOCK
    // =====================================================

    always #5 clk = ~clk;

    // =====================================================
    // INITIAL
    // =====================================================

    initial begin

        clk = 0;
        rst = 1;
        inp_valid = 0;

        // =================================================
        // LOAD MEM FILES
        // =================================================

        $readmemh("mnist_image.mem", image_mem);

        $readmemh("weight_1_0.mem", weight0_mem);
        $readmemh("weight_1_1.mem", weight1_mem);
        $readmemh("weight_1_2.mem", weight2_mem);

        $readmemh("bias_1.mem", bias_mem);

        // =================================================
        // IMAGE ZERO EXTENSION
        // =================================================

        for(i = 0; i < 784; i = i + 1) begin

            image[i] = {24'd0, image_mem[i]};

        end

        // =================================================
        // WEIGHT SIGN EXTENSION
        // =================================================

        for(i = 0; i < 25; i = i + 1) begin

            weight_0_0[i] =
                {{24{weight0_mem[i][7]}},
                weight0_mem[i]};

            weight_0_1[i] =
                {{24{weight1_mem[i][7]}},
                weight1_mem[i]};

            weight_0_2[i] =
                {{24{weight2_mem[i][7]}},
                weight2_mem[i]};

        end

        // =================================================
        // BIAS SIGN EXTENSION
        // =================================================

        for(i = 0; i < 3; i = i + 1) begin

            bias[i] =
                {{24{bias_mem[i][7]}},
                bias_mem[i]};

        end

        // =================================================
        // RESET
        // =================================================

        #20;

        rst = 0;

        // =================================================
        // START
        // =================================================

        #10;

        inp_valid = 1;

        #10;

        inp_valid = 0;

        // =================================================
        // WAIT
        // =================================================

        wait(valid_0 && valid_1 && valid_2);

        // =================================================
        // PRINT FILTER 0 OUTPUT
        // =================================================

        $display("=================================");
        $display("FILTER 0 OUTPUT");
        $display("=================================");

        for(i = 0; i < 144; i = i + 1) begin

            $write("%0d ", rout_0[i]);

            if((i + 1) % 12 == 0)
                $write("\n");

        end

        // =================================================
        // PRINT FILTER 1 OUTPUT
        // =================================================

        $display("=================================");
        $display("FILTER 1 OUTPUT");
        $display("=================================");

        for(i = 0; i < 144; i = i + 1) begin

            $write("%0d ", rout_1[i]);

            if((i + 1) % 12 == 0)
                $write("\n");

        end

        // =================================================
        // PRINT FILTER 2 OUTPUT
        // =================================================

        $display("=================================");
        $display("FILTER 2 OUTPUT");
        $display("=================================");

        for(i = 0; i < 144; i = i + 1) begin

            $write("%0d ", rout_2[i]);

            if((i + 1) % 12 == 0)
                $write("\n");

        end

        $display("=================================");

        // =====================================================
        // PRINT CONV 0 OUTPUT
        // =====================================================

        $display("=================================");
        $display("CONV 0 OUTPUT");
        $display("=================================");

        for(i = 0; i < 576; i = i + 1) begin

            $write("%0d ", dut.conv_0[i]);

            if((i + 1) % 24 == 0)
                $write("\n");

        end

        // =====================================================
        // PRINT CONV 1 OUTPUT
        // =====================================================

        $display("=================================");
        $display("CONV 1 OUTPUT");
        $display("=================================");

        for(i = 0; i < 576; i = i + 1) begin

            $write("%0d ", dut.conv_1[i]);

            if((i + 1) % 24 == 0)
                $write("\n");

        end

        // =====================================================
        // PRINT CONV 2 OUTPUT
        // =====================================================

        $display("=================================");
        $display("CONV 2 OUTPUT");
        $display("=================================");

        for(i = 0; i < 576; i = i + 1) begin

            $write("%0d ", dut.conv_2[i]);

            if((i + 1) % 24 == 0)
                $write("\n");

        end

        // =====================================================
        // PRINT POOL 0 OUTPUT
        // =====================================================

        $display("=================================");
        $display("POOL 0 OUTPUT");
        $display("=================================");

        for(i = 0; i < 144; i = i + 1) begin

            $write("%0d ", dut.pool_0[i]);

            if((i + 1) % 12 == 0)
                $write("\n");

        end

        // =====================================================
        // PRINT POOL 1 OUTPUT
        // =====================================================

        $display("=================================");
        $display("POOL 1 OUTPUT");
        $display("=================================");

        for(i = 0; i < 144; i = i + 1) begin

            $write("%0d ", dut.pool_1[i]);

            if((i + 1) % 12 == 0)
                $write("\n");

        end

        // =====================================================
        // PRINT POOL 2 OUTPUT
        // =====================================================

        $display("=================================");
        $display("POOL 2 OUTPUT");
        $display("=================================");

        for(i = 0; i < 144; i = i + 1) begin

            $write("%0d ", dut.pool_2[i]);

            if((i + 1) % 12 == 0)
                $write("\n");

        end

        #100;

        $finish;

    end

endmodule